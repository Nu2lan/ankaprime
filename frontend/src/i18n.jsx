const LangCtx = createContext()

export function LangProvider({ children }) {
    const [lang, setLangState] = useState(() => localStorage.getItem('lang') || 'az')

    const setLang = (l) => { setLangState(l); localStorage.setItem('lang', l) }
    const t = (key) => {
        const val = translations[lang]?.[key]
        return val !== undefined ? val : (translations.en[key] ?? key)
    }

    return <LangCtx.Provider value={{ lang, setLang, t }}>{children}</LangCtx.Provider>
}

export function useLang() { return useContext(LangCtx) }

/** Helper: pick the current-language value from a localized object or string */
export function useLocalized() {
    const { lang } = useLang()
    return (val) => {
        if (!val) return ''
        if (typeof val === 'string') return val
        return val[lang] || val.en || val.az || val.ru || ''
    }
}
